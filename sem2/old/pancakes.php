                        <li>2 cups whole milk</li>
                        <li>1/2 cup butter, melted</li>
                        <li>1-1/4 cups all-purpose flour</li>
                        <li>2 tablespoons plus 2-1/2 teaspoons sugar, divided</li>
                        <li>3-3/4 cups vanilla ice cream</li>
