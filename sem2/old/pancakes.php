            <div class="directions">  
                <div class="directions-body">
                    <div class="directions-header">
                        <h3>Directions</h3>
                    </div>
                    <ol>
                        <li>
                            In a large bowl, whisk eggs, milk and butter. 
                            In a small bowl, mix flour and 2 tablespoons sugar; add to egg mixture and mix well. 
                            Refrigerate, covered, 1 hour.
                        </li>
                        <li>
                            Heat a lightly greased 8-in. nonstick skillet over medium heat. Stir batter. 
                            Fill a 1/4-cup measure three-fourths with batter; pour into center of pan. 
                            Quickly lift, tilt and rotate pan to coat bottom evenly. 
                            Cook until top appears dry; turn pancake and cook 15-20 seconds longer or until bottom is cooked. 
                            Remove to a wire rack. Sprinkle with 1/8 teaspoon sugar.
                        </li>
                        <li>
                            Repeat with remaining batter and sugar, greasing pan as needed. When cool, stack pancakes with pieces of waxed paper or paper towels. 
                            Top each pancake with 3 tablespoons ice cream. Roll up; top with berries. Serve immediately.
                        </li>
                    </ol>
